    public void habilitarB(int op) {
        switch (op) {
            case 1: //algo -> sÃ³ novo
                BTNOVO.setEnabled(true);
                BTSALVAR.setEnabled(false);
                BTPESQUISAR.setEnabled(true);
                BTEDITAR.setEnabled(false);
                BTEXCLUIR.setEnabled(false);
                BTCANCELAR.setEnabled(false);
                
                EDNOME.setEditable(false);
                EDEMAIL.setEditable(false);
                EDSENHA.setEditable(false);
                
                EDNOME.requestFocus();
                break;
            case 2: //novo -> salvar
                BTNOVO.setEnabled(false);
                BTSALVAR.setEnabled(true);
                BTPESQUISAR.setEnabled(true);
                BTEDITAR.setEnabled(false);
                BTEXCLUIR.setEnabled(false);
                BTCANCELAR.setEnabled(true);
                
                EDNOME.setEditable(true);
                EDEMAIL.setEditable(true);
                EDSENHA.setEditable(true);
                
                EDNOME.requestFocus();
                break;
            case 3: //selecionar algo na tabela
                BTNOVO.setEnabled(false);
                BTSALVAR.setEnabled(false);
                BTPESQUISAR.setEnabled(true);
                BTEDITAR.setEnabled(true);
                BTEXCLUIR.setEnabled(true);
                 BTCANCELAR.setEnabled(true);
                 
                 EDNOME.setEditable(true);
                EDEMAIL.setEditable(true);
                EDSENHA.setEditable(true);
                 
                EDNOME.requestFocus();
                break;
        }
    }
