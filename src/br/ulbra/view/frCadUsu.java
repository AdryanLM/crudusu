    private void btPrimeiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPrimeiroActionPerformed
        int numeroLinhas = tabela.getRowCount();
        if (numeroLinhas > 0) {
            tabela.setRowSelectionInterval(0, 0);
            irTab();
        }
    }//GEN-LAST:event_btPrimeiroActionPerformed

    private void btUltimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUltimoActionPerformed
        int numeroLinhas = tabela.getRowCount();
        if (numeroLinhas > 0) {
            tabela.setRowSelectionInterval(numeroLinhas - 1, numeroLinhas - 1);
            irTab();
        }
    }//GEN-LAST:event_btUltimoActionPerformed

    private void BTSALVARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTSALVARActionPerformed

        usuario u = new usuario();
        u.setNomeUsu(EDNOME.getText());
        u.setEmailUsu(EDEMAIL.getText());
        u.setSenhaUsu(EDSENHA.getText());

        usuarioDao dao = new usuarioDao();
        dao.create(u);
        EDNOME.setText("");
        EDEMAIL.setText("");
        EDSENHA.setText("");
        habilitarB(1);
        readJTable();
        TabCad.setSelectedIndex(1);
    }//GEN-LAST:event_BTSALVARActionPerformed

    private void btAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAnteriorActionPerformed
        int numeroLinhas = tabela.getRowCount();
        int ls = tabela.getSelectedRow();
        if (numeroLinhas > 0 && ls != 0) {
            tabela.setRowSelectionInterval(ls - 1, ls - 1);
            irTab();
        }
    }//GEN-LAST:event_btAnteriorActionPerformed

    private void btProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProximoActionPerformed
        int numeroLinhas = tabela.getRowCount();
        int ls = tabela.getSelectedRow();
        if (numeroLinhas > 0 && ls < numeroLinhas - 1) {
            tabela.setRowSelectionInterval(ls + 1, ls + 1);
            irTab();
        }
    }//GEN-LAST:event_btProximoActionPerformed

