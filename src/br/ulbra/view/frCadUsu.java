    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        usuarioDao pdao = new usuarioDao();

        for (usuario u : pdao.read()) {

            modelo.addRow(new Object[]{
                u.getIdUsu(),
                u.getNomeUsu(),
                u.getEmailUsu(),
                u.getSenhaUsu()
            });

        }

    }

    public void readForDesc(String nome) {

        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        usuarioDao pdao = new usuarioDao();

        for (usuario u : pdao.readForDesc(nome)) {

            modelo.addRow(new Object[]{
                u.getIdUsu(),
                u.getNomeUsu(),
                u.getEmailUsu(),
                u.getSenhaUsu()
            });

        }

    }

