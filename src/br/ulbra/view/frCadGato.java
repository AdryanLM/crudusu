    private void irTab() {
        if (tabela.getSelectedRow() != -1) {

            EDID.setText(tabela.getValueAt(tabela.getSelectedRow(), 0).toString());
            EDNOME.setText(tabela.getValueAt(tabela.getSelectedRow(), 1).toString());
            LISTAGENERO.setSelectedItem(tabela.getValueAt(tabela.getSelectedRow(), 2).toString());
            EDRACA.setText(tabela.getValueAt(tabela.getSelectedRow(), 3).toString());
            EDDATANASCIMENTO.setText(tabela.getValueAt(tabela.getSelectedRow(), 4).toString());
            EDTAMANHO.setText(tabela.getValueAt(tabela.getSelectedRow(), 5).toString());
            boolean vacinado = false;
            if (tabela.getValueAt(tabela.getSelectedRow(), 6).toString() == "SIM") {
                vacinado = true;
            }
            CBVACINADO.setSelected(vacinado);
            EDCOR.setText(tabela.getValueAt(tabela.getSelectedRow(), 7).toString());
            EDPESO.setText(tabela.getValueAt(tabela.getSelectedRow(), 8).toString());
            boolean microchip = false;
            if (tabela.getValueAt(tabela.getSelectedRow(), 9).toString() == "SIM") {
                microchip = true;
            }
            CBMICROCHIP.setSelected(microchip);
            EDDESC.setText(tabela.getValueAt(tabela.getSelectedRow(), 10).toString());
        }
    }
