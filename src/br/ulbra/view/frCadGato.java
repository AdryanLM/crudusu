    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        
        gatoDao pdao = new gatoDao();

        for (gato g : pdao.read()) {
            String vacinado = "NAO", microchip = "NAO";
            if (g.isVacinadoGato()){
                vacinado = "SIM";
            }
            if (g.isMicrochipGato()){
                microchip = "SIM";
            }
            
            modelo.addRow(new Object[]{
                g.getIdGato(),
                g.getNomeGato(),
                g.getGeneroGato(),
                g.getRacaGato(),
                g.getNascimentoGato(),
                g.getTamanhoGato(),
                vacinado,
                g.getCorGato(),
                g.getPesoGato(),
                microchip,
                g.getDescricaoGato()
            });

        }

    }

